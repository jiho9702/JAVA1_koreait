package com.kita.first.level2.practice2;

public class Practice03 {
	//필드
	private String name;
	private int age;

	//생성자
	public Practice03(String name, int age) {
		this.name = name;
		this.age = age;
	}
	
	//메소드
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
